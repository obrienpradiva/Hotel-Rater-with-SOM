            k = new Kohonen(Integer.parseInt(iteration.getText()), 
                    Double.parseDouble(alpha.getText()));
            DefaultTableModel model = (DefaultTableModel) learningTable.getModel();
            for(int i=1;i<model.getRowCount();i++)
            {
                model.removeRow(i);
            }
            String[][] mBobot = k.getClusteredData();
